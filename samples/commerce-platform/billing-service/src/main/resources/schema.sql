CREATE TABLE IF NOT EXISTS invoices ( id BIGSERIAL PRIMARY KEY, user_id BIGINT, amount NUMERIC );
CREATE TABLE IF NOT EXISTS subscriptions ( id BIGSERIAL PRIMARY KEY, user_id BIGINT, plan TEXT );
CREATE TABLE IF NOT EXISTS payments ( id BIGSERIAL PRIMARY KEY, invoice_id BIGINT, status TEXT );
CREATE TABLE IF NOT EXISTS refunds ( id BIGSERIAL PRIMARY KEY, payment_id BIGINT );
