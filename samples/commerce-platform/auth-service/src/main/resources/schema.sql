CREATE TABLE IF NOT EXISTS users (
  id BIGINT PRIMARY KEY,
  email VARCHAR(255) UNIQUE,
  password_hash VARCHAR(255)
);
CREATE TABLE IF NOT EXISTS roles ( id BIGINT PRIMARY KEY, name VARCHAR(64) );
CREATE TABLE IF NOT EXISTS permissions ( id BIGINT PRIMARY KEY, code VARCHAR(64) );
CREATE TABLE IF NOT EXISTS sessions ( id VARCHAR(64) PRIMARY KEY, user_id BIGINT );
CREATE TABLE IF NOT EXISTS refresh_tokens ( token VARCHAR(255) PRIMARY KEY, user_id BIGINT );
